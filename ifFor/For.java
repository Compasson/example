package ifFor;

public class For {

	public static void main(String[] args) {
		/*
		 * Оператор цикла FOR:
		 * 
		 * for (начальное_значение; условие; приращение) оператор
		 * условие - булево выражение, которое проверяет значение управляющей переменной цикла.	
		 * Фигурные скобки обязательны когда операторов много. {} - блок кода.
		 **/
        int x;
        
        for (x =0; x <10; x++)
        	System.out.println("Значение x: " + x);
	}

}
